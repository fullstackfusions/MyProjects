
// concatination of strings
y = "Geeks" + "For" + "Geeks"
console.log(y)

// addition of numbers
y = 2 + 3
console.log(y)

// subtraction
y = 5 - 3
console.log(y)

// multiplication
y = 5 * 5
console.log(y)

// division
y = 5 / 5
console.log(y)

// modulus (gives remainder of an integer division)
y = 5 % 4
console.log(y)

// exponentiation (power of the first operator)
y = 5 ** 3
console.log(y)

// increment (increase integer by 1)
a = 9
y = a++
console.log(y) // 9
console.log(a) // 10

// decrement (decrease integer by 1)
a = 9
y = a--
console.log(y) // 9
console.log(a) // 8

// unary (fastest and preferred way of converting something into a number)
a = 10
y = +a
console.log(y) // this will be a positive number
a = -10
y = +a
console.log(y)
console.log(a)

// negation (fastest and preferred way of converting something into a number)
a = 10
y = -a
console.log(y) // this will be a negative number
a = -10
y = -a
console.log(y)
console.log(a)